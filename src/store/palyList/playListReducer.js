const defaultState = {
  playLists: [],
  playIdx: 0
}

export const playListReducers = (state = defaultState, action) => {
  switch (action.type) {
    case SET_PLAY_LIST:
      return state = {...state, playLists: [...action.list]}
    case ADD_PLAY_LIST:
      const addPlayList = [...state.playLists]
      addPlayList.push(action.item)
      return state = {
        ...state,
        playLists: addPlayList
      }
    case REMOVE_PLAY_LIST:
      const removePlayList = [...state.playLists]
      removePlayList.splice(action.idx, 1)
      return state = {
        ...state,
        playLists: removePlayList
      }
    case PLAY_INDEX:
      return state = {...state, playIdx: action.idx}
    default:
      return state
  }
}
